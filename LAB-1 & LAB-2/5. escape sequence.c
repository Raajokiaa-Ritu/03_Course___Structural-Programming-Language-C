#include<stdio.h>

/*

there are many escape sequence:-
\n : newline
\t : tab(it creates four space)
\\ : print one backslash(\)
\? : print '?' mark
\' : print single(') quotation
\" : print single(") quotation

*/


int main()
{
    printf("hello world\n");  // using newline
    printf("hello \t world"); //using tab
    printf("\n hello \\ world"); // print \ sign
    printf("\n hello \? world"); // print ? sign
    printf("\n hello \' world"); // print ' sign
    printf("\n hello \" world"); // print " sign




    return 0;
}
